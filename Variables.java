class Variables {
    public static void main(String[] args) {
        // String variables
        String a = "Hello Java";
        System.out.println(a);
        String b = "Hello java programminng";
        System.out.println(b);

        String c;
        c = "java language";
        System.out.println(c);

        // integer Numbers
        int d = 25;
        System.out.println(d);
        int e = 30;
        System.out.println(e);

        int f;
        f = 10;
        System.out.println(f);
    }
}